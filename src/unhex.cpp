template <typename Rs, std::size_t Ln>
Rs parse_hex(const char* start) {
	static_assert(Ln % 2 == 0);
	Rs out = 0;
	size_t shift = (Ln-1) * 4;
	for (int i = 0; i < Ln; i++) {
		const uint8_t v = hex_lut[static_cast<unsigned char>(start[i])];
		out |= (v << shift);
		shift -= 4;
	}
	return out;
}